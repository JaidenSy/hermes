def write_handoff_via_ollama(
    task: str,
    session_name: str,
    partial_output: str,
    cause: str = "Claude Code usage limit reached",
) -> str:
    """Use a local Ollama model to write a resume handoff for any failed step.

    `cause` is recorded in the note so the next agent knows why work stopped
    (rate-limit vs. validation failure vs. timeout) — the resume path differs.
    """
    log.info(f"Writing handoff via Ollama for: {session_name} (cause: {cause})")

    prompt = f"""You are writing a handoff note for an AI agent that could not finish its task.
Your job: read what was done and write a clear, actionable handoff so the next agent can resume without any human help.

ORIGINAL TASK:
{task}

PARTIAL OUTPUT (everything that ran before timeout):
{partial_output[:4000]}

Write a handoff note in this exact markdown format:

## What Was Completed
(bullet list of finished work — be specific, include file names and line numbers if mentioned)

## In Progress At Timeout
(what was being worked on when time ran out)

## Still Needs To Be Done
(remaining steps to fully complete the original task)

## How To Resume
(exact commands or steps the next agent should run first)

## Files Modified
(list any files that were changed, created, or deleted)

Be specific and factual. Only include what is supported by the partial output above."""

    try:
        import json as _json
        import urllib.request as _urllib

        payload = _json.dumps({"model": "llama3.1:8b", "prompt": prompt, "stream": False}).encode()
        req = _urllib.Request(
            "http://localhost:11434/api/generate",
            data=payload,
            headers={"Content-Type": "application/json"},
            method="POST",
        )
        with _urllib.urlopen(req, timeout=120) as resp:
            response_text = _json.loads(resp.read().decode()).get("response", "").strip()

        if not response_text:
            log.error("Ollama handoff generation returned empty response")
            return ""

        handoff_dir = Path.home() / "Documents" / "RaphBrain" / "Projects" / "handoffs"
        handoff_dir.mkdir(parents=True, exist_ok=True)
        handoff_path = handoff_dir / f"{session_name}-handoff.md"
        handoff_path.write_text(
            f"# Auto-Handoff — {session_name}\n"
            f"*Generated by Ollama — cause: {cause}*\n"
            f"*{datetime.now().strftime('%Y-%m-%d %H:%M')}*\n\n"
            f"> **Original task:** {task[:200]}\n\n"
            f"{response_text}\n"
        )
        log.info(f"Handoff written: {handoff_path}")
        return str(handoff_path)
    except TimeoutError:
        log.error("Ollama handoff timed out after 120s")
        return ""
    except OSError:
        log.error("Ollama not found — is it installed?")
        return ""


# ---------------------------------------------------------------------------
# Handoff lifecycle — a failed step already writes a resume handoff; these let
# Jaiden actually work through them from the phone (list → resume → archive)
# instead of opening Jump Desktop. Canonical store is the flat handoffs/ dir;
# resolved ones move into handoffs/resolved/ so the pending list stays honest.
# ---------------------------------------------------------------------------
HANDOFFS_DIR = Path.home() / "Documents" / "RaphBrain" / "Projects" / "handoffs"
HANDOFFS_RESOLVED_DIR = HANDOFFS_DIR / "resolved"


def _pending_handoffs() -> list:
    """Unresolved handoff files, newest first. The top-level glob skips resolved/."""
    if not HANDOFFS_DIR.exists():
        return []
    files = [p for p in HANDOFFS_DIR.glob("*-handoff.md") if p.is_file()]
    return sorted(files, key=lambda p: p.stat().st_mtime, reverse=True)


def _parse_handoff_meta(text: str) -> dict:
    """Pull {project, task} from a handoff. project comes from the
    `<!-- hermes-meta project="X" -->` marker; task from the Original task line.
    Both best-effort — a legacy handoff with neither still lists, just can't
    auto-route (resume falls back to inferring the project)."""
    import re

    meta = {"project": "", "task": ""}
    m = re.search(r'hermes-meta[^>]*project="([^"]*)"', text)
    if m:
        meta["project"] = m.group(1)
    tm = re.search(r"original task:\**\s*(.+)", text, re.I)
    if tm:
        meta["task"] = tm.group(1).strip().rstrip("*").strip()
    return meta


def _list_handoffs() -> str:
    pend = _pending_handoffs()
    if not pend:
        return "✅ No pending handoffs."
    now = datetime.now(timezone.utc).strftime("%Y-%m-%dT%H:%M:%SZ")
    lines = ["Pending handoffs — `resume <n>` to pick up, `handoffs clear` to dismiss all:"]
    for i, p in enumerate(pend, 1):
        try:
            meta = _parse_handoff_meta(p.read_text())
        except OSError:
            meta = {"project": "?", "task": "(unreadable)"}
        mtime = datetime.fromtimestamp(p.stat().st_mtime, timezone.utc).strftime(
            "%Y-%m-%dT%H:%M:%SZ"
        )
        lines.append(
            f"{i}. [{meta['project'] or '?'}] {(meta['task'] or '')[:48]} "
            f"— {_format_duration(mtime, now)} ago  ({p.stem})"
        )
    return "\n".join(lines)


def _clear_handoffs() -> str:
    pend = _pending_handoffs()
    if not pend:
        return "✅ No pending handoffs."
    HANDOFFS_RESOLVED_DIR.mkdir(parents=True, exist_ok=True)
    moved = 0
    for p in pend:
        try:
            p.rename(HANDOFFS_RESOLVED_DIR / p.name)
            moved += 1
        except OSError as exc:
            log.warning(f"[handoffs] could not archive {p.name}: {exc}")
    return f"🧹 Cleared {moved} handoff(s) → resolved/."


def _resume_handoff(arg: str, config: dict) -> str:
    """Resume a pending handoff: seed a fresh agent with it in the right repo,
    then archive the handoff (a new failure writes a fresh one, so the pending
    list always reflects reality). `arg` = an index into the pending list (empty →
    just list them), with an optional trailing project override (`2 alphabot`)."""
    pend = _pending_handoffs()
    if not pend:
        return "✅ No pending handoffs to resume. Send `handoffs` to check."

    parts = arg.split()
    sel = parts[0] if parts else ""
    override = parts[1] if len(parts) > 1 else ""

    if not sel:  # bare `resume` → just show the list to pick from
        return _list_handoffs()

    # A numeric selector is ONLY an index — an out-of-range number must fail, never
    # fall through to a substring match (stems embed unix timestamps full of digits,
    # so `resume 9` would silently match and resume the WRONG handoff).
    if sel.isdigit():
        i = int(sel)
        chosen = pend[i - 1] if 1 <= i <= len(pend) else None
    else:
        chosen = next((p for p in pend if sel in p.stem), None)
    if chosen is None:
        return f"❓ No pending handoff matches {sel!r}. Send `handoffs` to list them."

    try:
        text = chosen.read_text()
    except OSError as exc:
        return f"❓ Couldn't read handoff `{chosen.stem}`: {exc}"
    meta = _parse_handoff_meta(text)
    project = override or meta["project"]
    if not project and meta["task"]:
        try:
            project = classify_task(meta["task"]).project or ""
        except Exception:
            project = ""
    proj = resolve_project(project) if project else None
    if not proj or not proj.repo:
        guessed = f" (guessed {project!r})" if project else ""
        return (
            f"❓ Couldn't map handoff `{chosen.stem}` to a repo{guessed}.\n"
            f"Retry with a project: `resume {sel} <project>`."
        )

    resume_task = (
        "RESUME an unfinished task. A previous agent stopped partway and left the "
        "handoff below. Read it, pick up exactly where it left off, and finish the "
        "ORIGINAL TASK — do not restart from scratch.\n\n"
        f"ORIGINAL TASK: {meta['task'] or '(see handoff)'}\n\n"
        f"--- HANDOFF ---\n{text}"
    )
    ack = run_task(
        resume_task,
        on_complete=lambda r: _send_reply(config, r[:4096]),
        project=project,
        cwd=proj.repo,
    )
    try:
        HANDOFFS_RESOLVED_DIR.mkdir(parents=True, exist_ok=True)
        chosen.rename(HANDOFFS_RESOLVED_DIR / chosen.name)
    except OSError as exc:
        log.warning(f"[handoffs] archive-on-resume failed for {chosen.name}: {exc}")
    return f"▶ Resuming `{chosen.stem}` in {project}.\n{ack}"


# Hard safety kill for a direct task's agent subprocess. Real work finishes in
# minutes; this only backstops a hung/looping agent so it can't run forever.
# ponytail: single fixed ceiling — make it per-task config if tasks ever vary wildly.
DIRECT_SAFETY_TIMEOUT_S = 7200  # 2h

# Live direct tasks, so `status` can show them and `abort` can kill them.
# session_name -> {"task", "project", "started", "proc"}
_DIRECT_TASKS: dict = {}
_DIRECT_LOCK = threading.Lock()


def _resolve_claude_bin() -> str:
    """Absolute path to the claude CLI — a launchd daemon's PATH can be minimal."""
    return shutil.which("claude") or str(Path.home() / ".local" / "bin" / "claude")


def run_task(
    task: str,
    session_name: Optional[str] = None,
    on_complete: Optional[Callable[[str], None]] = None,
    project: Optional[str] = None,
    cwd: Optional[str] = None,
) -> str:
    """Run a direct task via `claude --print` in a background thread.

    Returns immediately with a short ack. When the agent process exits (or is
    killed after DIRECT_SAFETY_TIMEOUT_S) the captured output is handed to
    on_complete — so the daemon never blocks and a slow task is never falsely
    reported as timed out. `cwd` (a project repo) is where the agent runs;
    `project` is echoed in the ack so a wrong route is visible immediately.
    """
    if not session_name:
        session_name = f"hermes-{int(time.time())}"

    log.info(f"Spawning direct task: {session_name}")
    log.info(f"Task: {task[:120]}...")

    task_file = Path.home() / "hermes" / "tasks" / f"{session_name}.md"
    task_file.parent.mkdir(parents=True, exist_ok=True)
    task_file.write_text(f"# Hermes Task\n\n{task}\n")
    log_file = Path.home() / "hermes" / "logs" / f"{session_name}.log"
    log_file.parent.mkdir(parents=True, exist_ok=True)

    # Direct tasks default to sonnet — the classifier sometimes routes real work
    # here, and the cheapest model bounces on anything non-trivial.
    try:
        direct_cfg = load_config().get("models", {}).get("agent_models", {}).get("direct", {})
    except Exception:
        direct_cfg = {}
    direct_model = apply_downgrade(direct_cfg.get("model", "sonnet"))
    direct_max_turns = int(direct_cfg.get("max_turns", 30))

    cmd = [
        _resolve_claude_bin(),
        "--print",
        f"{project_map_text()}\n\n---\n\n{task}",
        "--model",
        str(direct_model),
        "--max-turns",
        str(direct_max_turns),
        # acceptEdits auto-approves file edits; --allowed-tools pre-approves the
        # read/inspect tools a phone status-check needs (ps, psql, git log, …) —
        # otherwise Bash is auto-denied in this headless run and "is X running?"
        # can't actually check anything.
        "--permission-mode",
        "acceptEdits",
        # Variadic — keep last so it collects all four tool names.
        "--allowed-tools",
        "Bash",
        "Read",
        "Grep",
        "Glob",
    ]

    run_cwd = str(Path(cwd).expanduser()) if cwd else str(Path.home())

    def _worker() -> None:
        result = ""
        try:
            proc = subprocess.Popen(
                cmd,
                cwd=run_cwd,
                stdout=subprocess.PIPE,
                stderr=subprocess.STDOUT,
                text=True,
            )
            with _DIRECT_LOCK:
                _DIRECT_TASKS[session_name] = {
                    "task": task,
                    "project": project,
                    "started": time.time(),
                    "proc": proc,
                }
            try:
                out, _ = proc.communicate(timeout=DIRECT_SAFETY_TIMEOUT_S)
                result = out.strip() or "(agent produced no output)"
                if proc.returncode and proc.returncode < 0:  # killed by a signal (abort)
                    result = f"🛑 Aborted.\n{result}"
            except subprocess.TimeoutExpired:
                proc.kill()
                out, _ = proc.communicate()
                hrs = DIRECT_SAFETY_TIMEOUT_S // 3600
                log.warning(f"Direct task hard-killed after {hrs}h: {session_name}")
                result = f"{(out or '').strip()}\n\n⚠️ Killed after {hrs}h safety timeout."
            log_file.write_text(result)
            log.info(f"Direct task complete: {session_name}")
        except Exception as exc:
            log.error(f"Direct task {session_name} failed: {exc}", exc_info=True)
            result = f"❌ Task {session_name} failed: {exc}"
        finally:
            with _DIRECT_LOCK:
                _DIRECT_TASKS.pop(session_name, None)

        if on_complete:
            try:
                on_complete(result)
            except Exception as exc:
                log.error(f"on_complete callback failed for {session_name}: {exc}")
        else:
            log.info(f"Direct task {session_name} result (no callback): {result[:200]}")

    threading.Thread(target=_worker, daemon=True, name=f"hermes-task-{session_name}").start()
    tag = f" (project: {project})" if project else ""
    return f"▶ Started {session_name}{tag} — I'll send the result when it's done."


